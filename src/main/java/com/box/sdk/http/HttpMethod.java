package com.box.sdk.http;

/**
 * HTTP method constants.
 */
public enum HttpMethod {
    /**
     * HTTP GET method.
     */
    GET,

    /**
     * HTTP HEAD method.
     */
    HEAD,

    /**
     * HTTP POST method.
     */
    POST,

    /**
     * HTTP PUT method.
     */
    PUT,

    /**
     * HTTP PATCH method.
     */
    PATCH,

    /**
     * HTTP DELETE method.
     */
    DELETE,

    /**
     * HTTP OPTIONS method.
     */
    OPTIONS,

    /**
     * HTTP TRACE method.
     */
    TRACE;
}
